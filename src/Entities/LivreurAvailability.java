package Entities;

/**
 *
 * @author Cheima
 */
public class LivreurAvailability {
    private int id;
    private int id_livreur;
    private boolean status;
    private String reason;

    public LivreurAvailability() {}

    public LivreurAvailability(int id, int id_livreur, boolean status, String reason) {
        this.id = id;
        this.id_livreur = id_livreur;
        this.status = status;
        this.reason = reason;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId_livreur() {
        return id_livreur;
    }

    public void setId_livreur(int id_livreur) {
        this.id_livreur = id_livreur;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }
    
}

